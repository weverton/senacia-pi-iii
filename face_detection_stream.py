logging.basicConfig(level=logging.INFO, format="%(message)s")
log = logging.getLogger(__name__)

# Ordem de prioridade das fontes de vídeo.
#
# Suporte a dois fluxos de celular:
#
# 1) App "IP Webcam" / "IP Camera" (Android) ou "DroidCam"/"EpocCam" (iOS):
#    o próprio celular vira um servidor e expõe uma URL HTTP/RTSP direta.
#    -> use a URL mostrada na tela do app, ex: "http://192.168.1.100:8080/video"
#
# 2) App "Larix Broadcaster": o celular ENVIA (push) o vídeo via RTMP ou RTSP
#    para um servidor no PC (ex: MediaMTX). O Python então lê do PRÓPRIO PC,
#    não do IP do celular.
#    -> rode o MediaMTX no PC (porta 8554 RTSP / 1935 RTMP)
#    -> configure no Larix: rtmp://IP_DO_PC:1935/live/cam (ou rtsp://IP_DO_PC:8554/cam)
#    -> aqui no Python, use o endereço RTSP local do MediaMTX:
#       "rtsp://127.0.0.1:8554/live/cam"
#
# Importante: cv2.VideoCapture lê RTMP/RTSP, mas não consegue "receber" um
# push do Larix diretamente — por isso o MediaMTX é necessário como
# intermediário no fluxo do Larix.
STREAM_URLS = [
    "http://192.168.1.100:8080/video",      # IP Webcam / IP Camera (ajuste o IP do celular)
    "rtsp://127.0.0.1:8554/live/cam",       # Larix -> MediaMTX (RTSP local, recomendado)
    "rtmp://127.0.0.1:1935/live/cam",       # Larix -> MediaMTX (lendo via RTMP, alternativa)
    "rtsp://192.168.1.100:8554/stream",     # RTSP genérico (ex: outro app/câmera IP real)
]

# Permite sobrescrever via variável de ambiente STREAM_URL ou --url na CLI,
# sem precisar editar o código a cada vez que o IP do celular mudar.
_env_url = os.environ.get("STREAM_URL")
if _env_url:
    STREAM_URLS.insert(0, _env_url)

# Webcams locais só são usadas como ÚLTIMO recurso (ver _connect_stream)
LOCAL_WEBCAM_IDS = [0, 1]

# Se True, tenta detectar automaticamente um IP Webcam na rede local
AUTO_DISCOVER_IP_WEBCAM = True

# Modelo YOLO para detecção facial
# Usa o modelo padrão YOLO e filtra por pessoas (classe 0)
MODEL_NAME = "yolov8n.pt"  # Nano - mais leve
# MODEL_NAME = "yolov8s.pt"  # Small - um pouco melhor
# MODEL_NAME = "yolov8m.pt"  # Medium - melhor performance

CONFIDENCE = 0.55
WINDOW_NAME = "Detecção Facial - Stream"
DISPLAY_WIDTH = 1280

# Diretório para salvar faces
SAVE_DIR = "faces"
SAVE_FACES = True
SAVE_COOLDOWN_SECONDS = 5

# Filtros
MIN_FACE_WIDTH = 60
MIN_FACE_HEIGHT = 60
BLUR_THRESHOLD = 80

# Performance
MAX_INFERENCE_FPS = 15
RECONNECT_DELAY = 2
CONNECT_TIMEOUT_PER_URL = 3  # segundos para testar cada URL

