class StreamManager:
    def __init__(self):
        self.cap = None
        self.cap_lock = threading.Lock()
        self.frame_queue = Queue(maxsize=2)
        self.running = False
        self.thread = None
        self.last_error = None

    def start(self):
        """Inicia o stream em uma thread separada."""
        self.running = True
        self.thread = threading.Thread(target=self._stream_worker, daemon=True)
        self.thread.start()

    # -------------------------------------------------
    # Descoberta / conexão
    # -------------------------------------------------

    def _try_open(self, source, backend=None, label=None):
        """Tenta abrir e validar uma fonte de vídeo (URL ou índice de webcam).
        Retorna o VideoCapture se funcionar, ou None."""
        label = label if label is not None else source
        log.info(f"🔍 Tentando: {label}")
        try:
            cap = cv2.VideoCapture(source, backend) if backend is not None else cv2.VideoCapture(source)
            if not cap.isOpened():
                cap.release()
                return None

            ret, frame = cap.read()
            if not ret or frame is None:
                cap.release()
                return None

            cap.set(cv2.CAP_PROP_BUFFERSIZE, 1)
            log.info(f"✅ Conectado a: {label}")
            return cap
        except Exception as e:
            log.warning(f"❌ Erro ao tentar {label}: {e}")
            return None

    def _discover_ip_webcam(self):
        """Tenta encontrar um IP Webcam ativo na rede local (portas comuns)."""
        for port in (8080, 8081, 8082, 8083):
            url = f"http://localhost:{port}/video"
            try:
                response = requests.get(url, timeout=1)
                if response.status_code == 200:
                    return url
            except requests.RequestException:
                continue
        return None

    def _connect_stream(self):
        """Tenta conectar, em ordem de prioridade:
        1) URLs configuradas em STREAM_URLS (IP Webcam / RTSP reais)
        2) IP Webcam descoberto automaticamente na rede
        3) Webcam local, como último recurso
        """
        # 1) URLs configuradas
        for url in STREAM_URLS:
            cap = self._try_open(url)
            if cap is not None:
                return cap

        # 2) Auto-descoberta de IP Webcam
        if AUTO_DISCOVER_IP_WEBCAM:
            discovered = self._discover_ip_webcam()
            if discovered:
                cap = self._try_open(discovered)
                if cap is not None:
                    return cap

        # 3) Webcam local — apenas como último recurso
        log.info("🔄 Nenhuma URL de rede funcionou. Tentando webcam local...")
        for cam_id in LOCAL_WEBCAM_IDS:
            cap = self._try_open(cam_id, label=f"webcam local (ID {cam_id})")
            if cap is not None:
                return cap

        raise RuntimeError("❌ Nenhuma fonte de vídeo disponível!")

    # -------------------------------------------------
    # Worker
    # -------------------------------------------------

    def _stream_worker(self):
        """Worker thread para capturar frames."""
        while self.running:
            try:
                with self.cap_lock:
                    needs_connect = self.cap is None or not self.cap.isOpened()
                if needs_connect:
                    new_cap = self._connect_stream()
                    with self.cap_lock:
                        self.cap = new_cap
                    continue

                with self.cap_lock:
                    cap = self.cap
                ret, frame = cap.read() if cap is not None else (False, None)

                if not ret or frame is None:
                    log.warning("⚠️ Stream perdido, reconectando...")
                    with self.cap_lock:
                        if self.cap is not None:
                            self.cap.release()
                        self.cap = None
                    time.sleep(RECONNECT_DELAY)
                    continue

                while self.frame_queue.qsize() >= 2:
                    try:
                        self.frame_queue.get_nowait()
                    except Exception:
                        break

                self.frame_queue.put(frame)

            except Exception as e:
                self.last_error = str(e)
                log.error(f"❌ Erro no stream: {e}")
                time.sleep(RECONNECT_DELAY)

    def get_frame(self, timeout=1):
        """Obtém o último frame da fila."""
        try:
            return self.frame_queue.get(timeout=timeout)
        except Exception:
            return None

    def stop(self):
        """Para o stream."""
        self.running = False
        if self.thread:
            self.thread.join(timeout=2)
        with self.cap_lock:
            if self.cap:
                self.cap.release()
                self.cap = None

