def parse_args():
    parser = argparse.ArgumentParser(description="Detecção facial com YOLO + stream")
    parser.add_argument(
        "--url", "-u",
        help="URL do stream de vídeo (ex: http://192.168.0.42:8080/video). "
             "Tem prioridade sobre STREAM_URL (env) e STREAM_URLS no código."
    )
    parser.add_argument(
        "--larix",
        action="store_true",
        help="Atalho para usar o fluxo Larix + MediaMTX local "
             "(equivalente a --url rtsp://127.0.0.1:8554/<caminho>). "
             "Use --mediamtx-path para trocar o caminho da stream."
    )
    parser.add_argument(
        "--mediamtx-path",
        default="live/cam",
        help="Caminho configurado no MediaMTX/Larix (padrão: live/cam). "
             "Só tem efeito junto com --larix."
    )
    return parser.parse_known_args()[0]


def main():
    args = parse_args()

    if args.larix:
        larix_url = f"rtsp://127.0.0.1:8554/{args.mediamtx_path}"
        STREAM_URLS.insert(0, larix_url)
        log.info(f"📱 Modo Larix ativado. Lendo do MediaMTX local: {larix_url}")
        log.info("   (confirme que o Larix está transmitindo para esse mesmo caminho)")

    if args.url:
        STREAM_URLS.insert(0, args.url)
        log.info(f"🔗 Usando URL passada por --url: {args.url}")

    print("="*60)
    print("   DETECÇÃO FACIAL COM YOLO + STREAM")
    print("="*60)

    # Carrega modelo
    print("\n📦 Carregando modelo YOLO...")
    model = load_model()

    # Inicia stream
    print("\n📡 Conectando ao stream...")
    stream = StreamManager()
    stream.start()
    time.sleep(2)

    # Verifica conexão
    test_frame = stream.get_frame(timeout=3)
    if test_frame is None:
        print("⚠️ Nenhum frame recebido ainda. Dicas:")
        print("   - Confirme que STREAM_URLS aponta para o IP correto do celular")
        print("   - No app IP Webcam/IP Camera, veja o IP exibido na tela inicial")
        print("   - Usando Larix? Confirme que o MediaMTX está rodando no PC")
        print("     e que o app está transmitindo para o mesmo caminho (ex: live/cam)")
        print("   - VDO.ninja (página web) NÃO funciona aqui — use IP Webcam/RTSP")
    else:
        print("✅ Stream conectado!")

    # Cria detector e janela
    detector = FaceDetector(model)
    cv2.namedWindow(WINDOW_NAME, cv2.WINDOW_NORMAL)

    print("\n" + "="*60)
    print("🎯 DETECÇÃO EM EXECUÇÃO")
    print("   Pressione 'q' para sair")
    print("   Pressione 's' para salvar frame")
    print("   Pressione 'r' para resetar contadores")
    print("="*60 + "\n")

    try:
        while True:
            # Obtém frame
            frame = stream.get_frame(timeout=1)

            if frame is None:
                print("⏳ Aguardando frame...")
                continue

            # Processa frame
            processed_frame, _ = detector.process_frame(frame)

            # Exibe
            display = resize_keep_ratio(processed_frame, DISPLAY_WIDTH)
            cv2.imshow(WINDOW_NAME, display)

            # Controles
            key = cv2.waitKey(1) & 0xFF

            if key == ord('q'):
                print("\n🛑 Saindo...")
                break
            elif key == ord('s'):
                timestamp = time.strftime("%Y%m%d_%H%M%S")
                filename = f"frame_{timestamp}.jpg"
                cv2.imwrite(filename, frame)
                print(f"💾 Frame salvo: {filename}")
            elif key == ord('r'):
                detector.reset_counters()

    except KeyboardInterrupt:
        print("\n🛑 Interrompido pelo usuário.")

    finally:
        # Finalização
        stream.stop()
        cv2.destroyAllWindows()

        stats = detector.get_stats()
        print("\n" + "="*60)
        print("📊 RESUMO FINAL")
        print("="*60)
        print(f"Frames processados: {stats['frames']}")
        print(f"Total de detecções: {stats['detections']}")
        print(f"Faces salvas: {stats['saved']}")

        if stats['frames'] > 0:
            media_deteccoes_por_frame = stats['detections'] / stats['frames']
            print(f"Média de detecções por frame: {media_deteccoes_por_frame:.2f}")

        if stats['saved'] > 0:
            print(f"\n📁 Faces salvas em: {os.path.abspath(SAVE_DIR)}")

        print("="*60)
        print("✅ Processamento concluído!")

if __name__ == "__main__":
    main()

